package Week_2.Operator;

public class W03 {
    // 비교연산자 예제
    public static void main(String[] args) {
        System.out.println(10 > 9); // 10이 9보다 크니?: 응(true)
        System.out.println(10 >= 9);
        System.out.println(10 < 9);
        System.out.println(10 <= 9);
        System.out.println(10 == 10);
        System.out.println(10 == 9);
        System.out.println(10 != 9);
        System.out.println(10 != 10);
    }
}
